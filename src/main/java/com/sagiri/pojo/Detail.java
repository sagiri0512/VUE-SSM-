package com.sagiri.pojo;

public class Detail {

  private long did;
  private long pid;
  private String dImg;


  public long getDid() {
    return did;
  }

  public void setDid(long did) {
    this.did = did;
  }


  public long getPid() {
    return pid;
  }

  public void setPid(long pid) {
    this.pid = pid;
  }


  public String getDImg() {
    return dImg;
  }

  public void setDImg(String dImg) {
    this.dImg = dImg;
  }

}
